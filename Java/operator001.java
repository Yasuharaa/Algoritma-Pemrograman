package Latihanjava;
public class operator001 {
    public static void main(String [] args) {
        int P, L;
        double luas;
        //memberi nilai
        P = 20;
        L = 30;
        //proses perhitungan
        luas = P*L;
        //output
        System.out.println("Menghitung Luas Persegi Panjang");
        System.out.println("-------------------------------");
        System.out.println(+P+ " * " +L+ " = " +luas);
        System.out.println("-------------------------------");
        System.out.println("Panjang = " +P);
        System.out.println("Lebar   = " +L);
        System.out.println("-------------------------------");
        System.out.println("Luas Persegi Panjang = " +luas);
        System.out.println("");
    }
}
